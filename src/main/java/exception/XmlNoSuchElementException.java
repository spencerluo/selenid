package exception;

public class XmlNoSuchElementException extends RuntimeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public XmlNoSuchElementException() {
		super();
		
	}

	public XmlNoSuchElementException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public XmlNoSuchElementException(String message) {
		super(message);
		
	}

	public XmlNoSuchElementException(Throwable cause) {
		super(cause);
		
	}
	
	

}
